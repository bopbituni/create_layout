<h1>Paris</h1>
<p>Paris là thủ đô và thành phố đông dân nhất của Pháp, với
    diện tích 105 kilômét vuông (41 dặm vuông) và dân số ước tính chính thức của 2.140.526 dân như của
    Ngày 1 tháng 1 năm 2019. Kể từ thế kỷ 17, Paris là một trong những trung tâm tài chính, ngoại giao lớn của châu Âu,
    thương mại, thời trang, khoa học và nghệ thuật</p>
